def rule_label(rule_id: str, *, with_code: bool = True) -> str:
    """룰 코드를 한국어 라벨로 변환. 미정의 룰은 코드 그대로 반환."""
    name = RULE_NAMES_KR.get(rule_id)
    if name is None:
        return rule_id
    return f"{rule_id} · {name}" if with_code else name
